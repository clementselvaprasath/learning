    @Test
    public void testEmpty () {
        assertEquals ("", Boolfuck.interpret ("", ""));
    }

    @Test
    public void testSingleCommands () {
        assertEquals ("", Boolfuck.interpret ("<", ""));
        assertEquals ("", Boolfuck.interpret (">", ""));
        assertEquals ("", Boolfuck.interpret ("+", ""));
        assertEquals ("", Boolfuck.interpret (".", ""));
        assertEquals ("\u0000", Boolfuck.interpret (";", ""));
    }

    @Test
    public void testHelloWorld () {
        assertEquals ("Hello, world!\n", Boolfuck.interpret (";;;+;+;;+;+;+;+;+;+;;+;;+;;;+;;+;+;;+;;;+;;+;+;;+;+;;;;+;+;;+;;;+;;+;+;+;;;;;;;+;+;;+;;;+;+;;;+;+;;;;+;+;;+;;+;+;;+;;;+;;;+;;+;+;;+;;;+;+;;+;;+;+;+;;;;+;+;;;+;+;+;", ""));
    }

